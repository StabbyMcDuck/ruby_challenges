fav_color = 'pink'

case fav_color
when 'red'
	puts "Red like fire!"
when 'orange'
	puts "Aren't you glad I said orange?!"
when 'yellow'
	puts "Daffodils are so pretty!"
else
	puts "That's cool too...."
end

